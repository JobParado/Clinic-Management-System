    private void btnAssignAsDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignAsDoctorActionPerformed
        int selectedRow = table1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a patient to assign yourself as doctor.");
            return;
        }

        String fullname = table1.getValueAt(selectedRow, 0).toString();

        String url = "jdbc:mysql://localhost:3306/clinicsystemdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL";
        String user = "root";
        String pass = "";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection conn = DriverManager.getConnection(url, user, pass)) {
                String sql = "UPDATE patient SET doctor = ? WHERE fullname = ?";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, doctorUsername);
                pstmt.setString(2, fullname);

                int rowsUpdated = pstmt.executeUpdate();
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "You are now assigned as the doctor for " + fullname + ".");
                    btnRefreshActionPerformed(null); // Refresh table
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to assign doctor.");
                }
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAssignAsDoctorActionPerformed

