    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        String url = "jdbc:mysql://localhost:3306/clinicsystemdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL";
        String user = "root";
        String pass = "";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection conn = DriverManager.getConnection(url, user, pass)) {
                String sql = "SELECT fullname, age, gender, date, doctor, status FROM patient";
                try (PreparedStatement pstmt = conn.prepareStatement(sql);
                     java.sql.ResultSet rs = pstmt.executeQuery()) {

                    // Clear existing rows
                    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) table1.getModel();
                    model.setRowCount(0);

                    // Add rows from database
                    while (rs.next()) {
                        Object[] row = {
                            rs.getString("fullname"),
                            rs.getString("age"),
                            rs.getString("gender"),
                            rs.getString("date"),
                            rs.getString("doctor"),
                            rs.getString("status")
                        };
                        model.addRow(row);
                    }
                }
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRefreshActionPerformed

    private void btnDisplayDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayDetailsActionPerformed
        int selectedRow = table1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a patient to view details.");
            return;
        }

        String fullname = table1.getValueAt(selectedRow, 0).toString();

        String url = "jdbc:mysql://localhost:3306/clinicsystemdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL";
        String user = "root";
        String pass = "";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection conn = DriverManager.getConnection(url, user, pass)) {
                String sql = "SELECT details FROM details WHERE fullname = ?";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, fullname);

                try (java.sql.ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()) {
                        String patientDetails = rs.getString("details");
                        JOptionPane.showMessageDialog(this, "Patient: " + fullname + "\nDetails: " + patientDetails);
                    } else {
                        JOptionPane.showMessageDialog(this, "No details found for patient: " + fullname);
                    }
                }
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDisplayDetailsActionPerformed

    private void btnChangeStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeStatusActionPerformed
    int selectedRow = table1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a patient to change status.");
        return;
    }

    String fullname = table1.getValueAt(selectedRow, 0).toString();
    String newStatus = (String) jStatus.getSelectedItem();

    String url = "jdbc:mysql://localhost:3306/clinicsystemdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL";
    String user = "root";
    String pass = "";

    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        try (Connection conn = DriverManager.getConnection(url, user, pass)) {
            // Check if the logged-in doctor is assigned to this patient
            String checkSql = "SELECT doctor FROM patient WHERE fullname = ?";
            PreparedStatement checkStmt = conn.prepareStatement(checkSql);
            checkStmt.setString(1, fullname);
            try (java.sql.ResultSet rs = checkStmt.executeQuery()) {
                if (rs.next()) {
                    String assignedDoctor = rs.getString("doctor");
                    if (doctorUsername != null && doctorUsername.equalsIgnoreCase(assignedDoctor)) {
                        // Doctor is assigned, allow status change
                        String updateSql = "UPDATE patient SET status = ? WHERE fullname = ?";
                        PreparedStatement updateStmt = conn.prepareStatement(updateSql);
                        updateStmt.setString(1, newStatus);
                        updateStmt.setString(2, fullname);

                        int rowsUpdated = updateStmt.executeUpdate();
                        if (rowsUpdated > 0) {
                            JOptionPane.showMessageDialog(this, "Status updated successfully.");
                            btnRefreshActionPerformed(null);
                        } else {
                            JOptionPane.showMessageDialog(this, "Failed to update status.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "You can only change the status for patients assigned to you.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Patient not found.");
                }
            }
        }
    } catch (ClassNotFoundException e) {
        JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnChangeStatusActionPerformed

