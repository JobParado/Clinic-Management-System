    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        String url = "jdbc:mysql://localhost:3306/clinicsystemdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL";
        String user = "root";
        String pass = "";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection conn = DriverManager.getConnection(url, user, pass)) {
                String sql = "SELECT fullname, age, gender, date, doctor, status FROM patient";
                try (PreparedStatement pstmt = conn.prepareStatement(sql);
                     java.sql.ResultSet rs = pstmt.executeQuery()) {

                    // Clear existing rows
                    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) table1.getModel();
                    model.setRowCount(0);

                    // Add rows from database
                    while (rs.next()) {
                        Object[] row = {
                            rs.getString("fullname"),
                            rs.getString("age"),
                            rs.getString("gender"),
                            rs.getString("date"),
                            rs.getString("doctor"),
                            rs.getString("status")
                        };
                        model.addRow(row);
                    }
                }
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRefreshActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selectedRow = table1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a patient to remove.");
            return;
        }

        // Get the fullname from the selected row (assuming fullname is unique)
        String fullname = table1.getValueAt(selectedRow, 0).toString();

        String url = "jdbc:mysql://localhost:3306/clinicsystemdatabase?zeroDateTimeBehavior=CONVERT_TO_NULL";
        String user = "root";
        String pass = "";

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove patient: " + fullname + "?", "Confirm Remove", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection conn = DriverManager.getConnection(url, user, pass)) {
                // Remove from patient table
                String sqlPatient = "DELETE FROM patient WHERE fullname = ?";
                PreparedStatement pstmtPatient = conn.prepareStatement(sqlPatient);
                pstmtPatient.setString(1, fullname);
                pstmtPatient.executeUpdate();

                // Remove from details table
                String sqlDetails = "DELETE FROM details WHERE fullname = ?";
                PreparedStatement pstmtDetails = conn.prepareStatement(sqlDetails);
                pstmtDetails.setString(1, fullname);
                pstmtDetails.executeUpdate();

                // Remove from table1 (UI)
                javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) table1.getModel();
                model.removeRow(selectedRow);

                JOptionPane.showMessageDialog(this, "Patient removed successfully.");
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "MySQL JDBC Driver not found.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

